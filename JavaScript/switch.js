var int_number = 3;
switch (int_number) {
    case 1:
        console.log("".concat(int_number, " = 1"));
        break;
    case 2:
        console.log("".concat(int_number, " == 2"));
        break;
    case 3:
        console.log("".concat(int_number, " == 3"));
        break;
    default:
        console.log("".concat(int_number, " is not a number or > 3"));
}
